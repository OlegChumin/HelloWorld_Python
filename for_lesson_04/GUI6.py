# Чтобы изменить цвет текста и фона окна в Tkinter, нужно указать параметры fg (foreground) для цвета текста и
# bg (background) для фона.
#
# Пример изменения цвета текста и фона окна:
import tkinter as tk

# Создание главного окна
root = tk.Tk()

# Установка заголовка окна
root.title("Изменение цвета текста и фона")

# Установка размеров окна
root.geometry("300x200")

# Изменение цвета фона окна
root.configure(bg="lightblue")

# Создание текстовой метки с измененным цветом текста и фона
label = tk.Label(root, text="Привет, Мир!", font=("Arial", 20), fg="white", bg="blue")
label.pack(pady=50)

# Запуск главного цикла окна
root.mainloop()

# Как это работает:
# root.configure(bg="lightblue") — устанавливает цвет фона всего окна. В данном случае цвет фона окна светло-голубой.
# В Label() параметр fg="white" меняет цвет текста на белый.
# Параметр bg="blue" внутри Label() задает синий фон для текстовой метки.
# Дополнительные параметры:
# Ты можешь использовать стандартные названия цветов ("red", "green", "yellow", "black", и т.д.) или шестнадцатеричные
# цветовые коды ("#RRGGBB"), например:
# fg="#FF0000" — красный текст.
# bg="#0000FF" — синий фон.
# Таким образом, можно изменять внешний вид элементов интерфейса, задавая различные цвета для текста и фона.
