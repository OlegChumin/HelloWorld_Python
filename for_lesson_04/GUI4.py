# Вывод произвольного текста из поля ввода:
# Теперь добавим поле для ввода текста и кнопку, по нажатию на которую текст из поля будет выводиться на экран:
import tkinter as tk

def display_text():
    user_text = entry.get()
    label.config(text=user_text)

# Создание главного окна
root = tk.Tk()

# Установка заголовка окна
root.title("Введите текст")

# Установка размеров окна
root.geometry("400x200")

# Поле для ввода
entry = tk.Entry(root, width=30)
entry.pack(pady=20)

# Кнопка для отправки текста
button = tk.Button(root, text="Показать текст", command=display_text)
button.pack(pady=10)

# Метка для отображения текста
label = tk.Label(root, text="", font=("Arial", 20))
label.pack(pady=20)

# Запуск главного цикла окна
root.mainloop()

# Как работает:
# entry.get() позволяет получить текст из поля ввода.
# После нажатия на кнопку текст из поля ввода выводится в метке.
