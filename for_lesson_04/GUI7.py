# Для добавления кнопки, которая изменяет цвет текста и фона на чёрный фон и ярко-салатовый текст, можно
# воспользоваться функцией, которая обновляет параметры метки при нажатии на кнопку.
#
# Вот пример программы с такой кнопкой:

import tkinter as tk

def change_color():
    # Меняем цвет текста на ярко-салатовый и фон на черный
    label.config(fg="lime", bg="black")

# Создание главного окна
root = tk.Tk()

# Установка заголовка окна
root.title("Изменение цвета текста")

# Установка размеров окна
root.geometry("300x200")

# Изменение цвета фона окна
root.configure(bg="lightblue")

# Создание текстовой метки с начальным цветом текста и фона
label = tk.Label(root, text="Привет, Мир!", font=("Arial", 20), fg="black", bg="white")
label.pack(pady=20)

# Создание кнопки для изменения цвета
button = tk.Button(root, text="Изменить цвет", command=change_color)
button.pack(pady=10)

# Запуск главного цикла окна
root.mainloop()


# Как работает программа:
# Функция change_color() — меняет цвет текста (fg) на ярко-салатовый (lime), а фона (bg) на чёрный.
# Кнопка с текстом "Изменить цвет" привязана к этой функции через параметр command=change_color.
# После нажатия на кнопку цвет текста станет ярко-салатовым, а фон — чёрным.
# Этот пример показывает, как легко можно изменять стили элементов при взаимодействии с пользователем через кнопки.