# Чтобы запретить изменять размер окна в Tkinter, можно воспользоваться методом resizable().
# Этот метод позволяет указать, можно ли изменять размер окна по горизонтали и/или вертикали.
import tkinter as tk

# Создание главного окна
root = tk.Tk()

# Установка заголовка окна
root.title("Окно с фиксированным размером")

# Установка размеров окна
root.geometry("300x200")

# Запрет на изменение размеров окна
root.resizable(False, False)  # Первый аргумент — по горизонтали, второй — по вертикали

# Создание текстовой метки
label = tk.Label(root, text="Размер окна нельзя изменить", font=("Arial", 14))
label.pack(pady=50)

# Запуск главного цикла окна
root.mainloop()
