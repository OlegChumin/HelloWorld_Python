# Как это работает:
# Импортируем tkinter: Это основной модуль для работы с графикой в Python.
# Создаем главное окно с помощью Tk().
# Устанавливаем заголовок окна через root.title().
# Устанавливаем размеры окна через root.geometry().
# Добавляем текстовую метку с помощью Label(), где указываем текст и его шрифт.
# pack() метод автоматически размещает элемент в окне.
# Запускаем цикл обработки событий с помощью root.mainloop() — это основной цикл программы, который ждет действия пользователя.

import tkinter as tk

# Создание главного окна
root = tk.Tk()

# Установка заголовка окна
root.title("Текст в окне")

# Установка размеров окна
root.geometry("300x200")

# Создание текстовой метки
label = tk.Label(root, text="Hello, World!", font=("Arial", 20))
label.pack(pady=50)  # Позиционируем текст по центру с отступом

# Запуск главного цикла окна
root.mainloop()
