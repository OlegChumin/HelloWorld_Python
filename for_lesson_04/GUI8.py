# Чтобы реализовать переключение между двумя состояниями (цвет текста на чёрном фоне и ярко-салатовый текст, и обратно),
# можно использовать переменную-состояние. При каждом нажатии на кнопку мы будем изменять состояние и соответственно менять цвета.
#
# Вот пример программы с такой логикой:

import tkinter as tk

def change_color():
    global is_black  # Используем глобальную переменную для отслеживания состояния
    if is_black:
        # Меняем на стандартные цвета
        label.config(fg="black", bg="white")
    else:
        # Меняем на черный фон и ярко-салатовый текст
        label.config(fg="lime", bg="black")
    is_black = not is_black  # Переключаем состояние

# Создание главного окна
root = tk.Tk()

# Установка заголовка окна
root.title("Переключение цвета текста")

# Установка размеров окна
root.geometry("300x200")

# Изменение цвета фона окна
root.configure(bg="lightblue")

# Начальное состояние
is_black = False

# Создание текстовой метки с начальным цветом текста и фона
label = tk.Label(root, text="Привет, Мир!", font=("Arial", 20), fg="black", bg="white")
label.pack(pady=20)

# Создание кнопки для изменения цвета
button = tk.Button(root, text="Изменить цвет", command=change_color)
button.pack(pady=10)

# Запуск главного цикла окна
root.mainloop()
